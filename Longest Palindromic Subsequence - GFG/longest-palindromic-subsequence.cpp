class Solution{
  public:
  
  int lcs(string str1, string str2, int m, int n)
    {
        int dp[m+1][n+1];
        for(int i = 0; i < m+1; i++)
        {
            for(int j = 0; j < n+1; j++)
            {
                if(i == 0 || j == 0)
                    dp[i][j] = 0;
            }
        }
        for(int i = 1; i < m+1; i++)
        {
            for(int j = 1; j < n+1; j++)
            {
                if(str1[i-1] == str2[j-1])
                    dp[i][j] = 1 + dp[i-1][j-1];
                else
                    dp[i][j] = max(dp[i-1][j], dp[i][j-1]);
            }
        }
        return dp[m][n];
    }
    
    int longestPalinSubseq(string A) {
        
         int m = A.size();
        string B = "";
        // reverse(B.begin(), B.end());
        for(int i = m; i >= 0; i--)
            B.push_back(A[i]);
        int n = B.size();
        int ans = lcs(A, B, m, n);
        return ans;
    }
};

