class Solution
{
    public:
    //Function to rotate a linked list.
    Node* rotate(Node* head, int k)
    {
        // Your code here
        Node* temp = head;
        
        if(head == NULL || head->next == NULL || k == 0)
            return head;
        
        //int countlen = 0;
        
        while(temp->next != NULL)
        {
            temp = temp->next;
            //countlen++;
        }
        temp->next = head;
        
        // k = k % countlen;
        // k = countlen-k+1;
        
        while(k > 0)
        {
            temp = temp->next;
            k--;
        }
        head = temp->next;
        temp->next = NULL;
        return head;
    }
};
    


